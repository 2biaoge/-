/*
整数型 
byte short int long 
*/
public class Demo5{
	public static void main(String[] args){
		//声明变量并赋值	
		int num=10;	
		System.out.println(num);
		byte b1=127;  //-128 到127
		System.out.println(b1);
		short s1=100;
		System.out.println(s1);
		long l=1000L; //后面加上L或l 
		System.out.println(l);
		
	}
}