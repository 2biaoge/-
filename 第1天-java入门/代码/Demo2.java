public class Demo2{
	public static void main(String[] args){
		//输出小数
		System.out.printf("%f",3.14159);
		//输出换行
		System.out.println();
		//保留两位小数
		System.out.printf("%.2f",3.14159);
		System.out.println();
		//输出整数
		System.out.printf("%d",200);
		System.out.println();
		//输出字符串
		System.out.printf("%s","hello world");
		System.out.println();
		//输出十六进制
		System.out.printf("%X",20);
		
		
	}
}